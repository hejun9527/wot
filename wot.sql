#设置储存编码
SET NAMES UTF8;
#丢弃数据库，如果存在
DROP DATABASE IF EXISTS wot;
#创建数据库,设置储存编码
CREATE DATABASE wot CHARSET=UTF8;
#进入数据库
USE wot;
#用户表wot_user
CREATE TABLE swan_user(
	uid INT  PRIMARY KEY AUTO_INCREMENT,
	uname VARCHAR(32),
	upwd VARCHAR(32),
	email VARCHAR(64),
	phone VARCHAR(11),
	gebder INT
);



